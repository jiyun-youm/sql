--뷰 생성 (하나의 테이블로 생성된 뷰-단순뷰)
CREATE OR REPLACE VIEW VIEW_EMP
AS (
    SELECT EMPLOYEE_ID AS EMP_ID,
           FIRST_NAME ||' '||LAST_NAME AS NAME,
           JOB_ID,
           SALARY
    FROM EMPLOYEES
    WHERE DEPARTMENT_ID=60
);

--복합뷰-두개 이상의 테이블을 조인해서 만들어진 뷰
CREATE OR REPLACE VIEW VIEW_EMP
AS (
    SELECT E.EMPLOYEE_ID,
           E.FIRST_NAME||' '||E.LAST_NAME AS NAME,
           J.JOB_TITLE,
           D.DEPARTMENT_NAME,
           L.CITY,
           L.STREET_ADDRESS
    FROM EMPLOYEES E
    JOIN JOBS J
    ON E.J0B_ID=J.JOB_ID
    JOIN DEPARTMENTS D
    ON E.DEPARTMENT_ID=D.DEPARTMENT_ID
    JOIN LOCATIONS L
    ON D.LOCATION_ID=L.LOCATION_ID
);

--뷰를 생성하면 데이터의 조회가 용이해짐
SELECT JOB_TITLE,COUNT(*) AS CNT
FROM VIEW_EMP_JOB
GROUP BY JOB_TITLE
ORDER BY CNT;

--뷰의 수정(OR REPLACE)를 붙이면 생성구문과 동일함
CREATE OR REPLACE VIEW VIEW_EMP_JOB
AS (
    SELECT E.EMPLOYEE_ID,
           E.FIRST_NAME||' '||E.LAST_NAME AS NAME,
           J.JOB_TITLE,
           D.DEPARTMENT_NAME,
           L.CITY,
           L.STREET_ADDRESS
    FROM EMPLOYEES E
    JOIN JOBS J
    ON E.J0B_ID=J.JOB_ID
    JOIN DEPARTMENTS D
    ON E.DEPARTMENT_ID=D.DEPARTMENT_ID
    JOIN LOCATIONS L
    ON D.LOCATION_ID=L.LOCATION_ID
    WHERE DEPARTMENT_ID=60
)
ORDER BY EMPLOYEE_ID DESC;

--뷰의 삭제
DROP VIEW VIEW_EMP_JOB;
