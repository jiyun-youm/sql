--뷰의 옵션
--WITH CHECK OPTION-제약,WHERE절에 들어간 컬럼의 변경을 금지함
--WITH READ ONLY-SELECT만 허용함
CREATE OR REPLACE VIEW VIEW_EMP
AS(
    SELECT EMPLOYEE_ID,
           FIRST_NAME,
           JOB_ID
           DEPARTMENT_ID
    FROM EMPLOYEES
    WHERE DEPARTMENT_ID IN (50,60,70)
)WITH READ ONLY; --WITH CHECK OPTION;

--WHERE절에 있는 DEPARTMENT_ID는 변경이 불가
